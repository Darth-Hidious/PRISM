/// Bind PLACEHOLDER values for a step's declared outputs during dry runs,
/// so downstream template rendering can be planned without executing
/// anything. The placeholder names the producing step and field.
fn bind_placeholder_outputs(
    step: &WorkflowStep,
    context: &mut BTreeMap<String, serde_json::Value>,
) {
    if let Some(map) = step.config.get("outputs").and_then(|v| v.as_object()) {
        for (field, var) in map {
            let var_name = var.as_str().unwrap_or(field).trim_start_matches('$');
            context.insert(
                var_name.to_string(),
                serde_json::Value::String(format!("<pending:{}.{}>", step.id, field)),
            );
        }
    }
}

fn lookup_output_field<'a>(
    result: &'a serde_json::Value,
    field: &str,
) -> Option<&'a serde_json::Value> {
    if let Some(v) = result.get(field) {
        return Some(v);
    }
    for envelope in ["data", "output", "result"] {
        if let Some(v) = result.get(envelope).and_then(|e| e.get(field)) {
            return Some(v);
        }
    }
    None
}

/// Extract a JSON object from an LLM response — tolerates code fences and
/// surrounding prose, but requires a real object to be present.
fn extract_json_object(text: &str) -> Option<serde_json::Value> {
    let trimmed = text.trim();
    let unfenced = trimmed
        .strip_prefix("```json")
        .or_else(|| trimmed.strip_prefix("```"))
        .map(|t| t.strip_suffix("```").unwrap_or(t))
        .unwrap_or(trimmed)
        .trim();
    if let Ok(v) = serde_json::from_str::<serde_json::Value>(unfenced)
        && v.is_object()
    {
        return Some(v);
    }
    let start = unfenced.find('{')?;
    let end = unfenced.rfind('}')?;
    if end <= start {
        return None;
    }
    serde_json::from_str::<serde_json::Value>(&unfenced[start..=end])
        .ok()
        .filter(|v| v.is_object())
}

fn render_value(
    value: serde_json::Value,
    context: &BTreeMap<String, serde_json::Value>,
) -> Result<serde_json::Value> {
    match value {
        serde_json::Value::String(text) => render_string(&text, context),
        serde_json::Value::Array(items) => {
            let rendered = items
                .into_iter()
                .map(|item| render_value(item, context))
                .collect::<Result<Vec<_>>>()?;
            Ok(serde_json::Value::Array(rendered))
        }
        serde_json::Value::Object(map) => {
            let mut rendered = serde_json::Map::new();
            for (key, value) in map {
                rendered.insert(key, render_value(value, context)?);
            }
            Ok(serde_json::Value::Object(rendered))
        }
        other => Ok(other),
    }
}

fn render_string(
    text: &str,
    context: &BTreeMap<String, serde_json::Value>,
) -> Result<serde_json::Value> {
    if !text.contains("{{") {
        return Ok(serde_json::Value::String(text.to_string()));
    }
    if let Some(path) = whole_template(text) {
        return resolve_path(context, path);
    }

    let mut rendered = String::new();
    let mut rest = text;
    while let Some(start) = rest.find("{{") {
        let (before, after_start) = rest.split_at(start);
        rendered.push_str(before);
        let after_start = &after_start[2..];
        let end = after_start
            .find("}}")
            .ok_or_else(|| anyhow!("unterminated workflow template in '{text}'"))?;
        let key = after_start[..end].trim();
        let resolved = resolve_path(context, key)?;
        rendered.push_str(&interpolated_display(&resolved));
        rest = &after_start[end + 2..];
    }
    rendered.push_str(rest);
    Ok(serde_json::Value::String(rendered))
}

fn whole_template(text: &str) -> Option<&str> {
    let trimmed = text.trim();
    if trimmed.starts_with("{{") && trimmed.ends_with("}}") {
        let inner = &trimmed[2..trimmed.len() - 2];
        if !inner.contains("{{") && !inner.contains("}}") {
            return Some(inner.trim());
        }
    }
    None
}

fn resolve_path(
    context: &BTreeMap<String, serde_json::Value>,
    path: &str,
) -> Result<serde_json::Value> {
    // `args.` is a documented convention, not a real container: docs/workflows.md
    // promises `{{ args.formula }}` ≡ `{{ formula }}`, and every LLM-authoring
    // example writes the `args.` form. Arguments/values land in the context flat
    // (keyed by bare name), so strip a leading `args.` before resolving — unless
    // a workflow genuinely put an `args` object in context, in which case honor
    // that literal. Without this, workflows written straight from the guide fail
    // their first render with "unknown workflow context path".
    let path = if path.starts_with("args.") && !context.contains_key("args") {
        path.strip_prefix("args.").unwrap_or(path)
    } else {
        path
    };
    let mut current = context
        .get(path.split('.').next().unwrap_or_default())
        .cloned()
        .ok_or_else(|| anyhow!("unknown workflow context path: {path}"))?;
    for segment in path.split('.').skip(1) {
        current = match current {
            serde_json::Value::Object(map) => map
                .get(segment)
                .cloned()
                .ok_or_else(|| anyhow!("unknown workflow context path: {path}"))?,
            _ => bail!("unknown workflow context path: {path}"),
        };
    }
    Ok(current)
}

/// SSRF guard for workflow `http` steps. Returns `Some(reason)` when
/// the URL must be rejected, `None` when it's acceptable.
///
/// Blocks:
///   - non-http(s) schemes (file://, gopher://, dict://, data://, …)
///   - empty / unparseable URLs
///   - hostnames that resolve to local / metadata-service contexts:
///     `localhost`, `0.0.0.0`, RFC1918 / loopback / link-local literals,
///     and the well-known cloud metadata service IP `169.254.169.254`
///   - common metadata hostnames: `metadata`, `metadata.google.internal`
///
/// What this does NOT do (and why a future allowlist is the right
/// long-term fix): defend against DNS rebinding (resolves to a public
/// IP at parse time, then to an internal IP at request time), or any
/// host that an attacker controls and points at internal infra. Users
/// running marketplace workflows against sensitive networks should add
/// a hostname allowlist to their workflow runtime config.
fn ssrf_block_reason(url: &str) -> Option<&'static str> {
    // reqwest::Url is the same `url::Url` from the url crate; using it
    // via reqwest avoids adding a top-level dependency just for parse.
    let Ok(parsed) = reqwest::Url::parse(url) else {
        return Some("URL did not parse");
    };

    // 1. Scheme: only http(s).
    match parsed.scheme() {
        "http" | "https" => {}
        _ => return Some("only http(s) URLs are allowed"),
    }

    let Some(host) = parsed.host_str() else {
        return Some("URL has no host");
    };
    let host = host.to_ascii_lowercase();

    // 2. Literal hostnames that name local / metadata contexts.
    const BLOCKED_HOSTS: &[&str] = &[
        "localhost",
        "ip6-localhost",
        "ip6-loopback",
        "metadata",
        "metadata.google.internal",
    ];
    if BLOCKED_HOSTS.contains(&host.as_str()) {
        return Some("hostname names a loopback or cloud-metadata service");
    }

    // 3. Reject literal IPs that fall into private / loopback / link-local
    // ranges. We don't do DNS lookup here — DNS rebinding still works.
    // The literal-IP check stops the obvious SSRF payloads.
    if let Ok(ipv4) = host.parse::<std::net::Ipv4Addr>() {
        if ipv4.is_loopback()
            || ipv4.is_private()
            || ipv4.is_link_local()
            || ipv4.is_unspecified()
            || ipv4.is_broadcast()
        {
            return Some("IPv4 address is in a private / loopback / link-local range");
        }
        // 169.254.169.254 is link-local but call it out specifically.
        if ipv4.octets() == [169, 254, 169, 254] {
            return Some("address is the cloud instance metadata service");
        }
    }
    // url crate strips the [] from IPv6 hosts, but the host_str() form
    // returns it WITH the brackets. Strip them before parsing.
    let ipv6_candidate = host
        .strip_prefix('[')
        .and_then(|s| s.strip_suffix(']'))
        .unwrap_or(host.as_str());
    if let Ok(ipv6) = ipv6_candidate.parse::<std::net::Ipv6Addr>()
        && (ipv6.is_loopback() || ipv6.is_unspecified())
    {
        return Some("IPv6 address is loopback / unspecified");
    }

    None
}

